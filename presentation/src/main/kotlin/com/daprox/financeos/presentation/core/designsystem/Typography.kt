// Google Fonts provider — Inter is fetched at runtime via Google Play Services.
// The certificate array (R.array.com_google_android_gms_fonts_certs) is declared
// in presentation/src/main/res/values/font_certs.xml.
private val provider = GoogleFont.Provider(
    providerAuthority = "com.google.android.gms.fonts",
    providerPackage = "com.google.android.gms",
    certificates = R.array.com_google_android_gms_fonts_certs,
)

private val InterFont = GoogleFont("Inter")

// Four weights cover the full type scale — Normal (body), Medium (labels/titles),
// SemiBold (headlines), Bold (display).
private val InterFontFamily = FontFamily(
    Font(googleFont = InterFont, fontProvider = provider, weight = FontWeight.Normal),
    Font(googleFont = InterFont, fontProvider = provider, weight = FontWeight.Medium),
    Font(googleFont = InterFont, fontProvider = provider, weight = FontWeight.SemiBold),
    Font(googleFont = InterFont, fontProvider = provider, weight = FontWeight.Bold),
)

